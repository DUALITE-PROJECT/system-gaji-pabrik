-- 1. Pastikan tabel ada
CREATE TABLE IF NOT EXISTS public.karyawan_pabrik (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    kode TEXT NOT NULL,
    nama TEXT NOT NULL,
    jenis_kelamin TEXT DEFAULT 'L',
    grade_p1 TEXT,
    grade_p2 TEXT,
    divisi TEXT,
    bulan TEXT,
    keterangan TEXT,
    status_aktif BOOLEAN DEFAULT true,
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- 2. Hapus constraint lama jika ada (untuk mencegah error)
ALTER TABLE public.karyawan_pabrik DROP CONSTRAINT IF EXISTS unique_karyawan_kode_bulan;

-- 3. Tambahkan constraint unik yang benar
ALTER TABLE public.karyawan_pabrik ADD CONSTRAINT unique_karyawan_kode_bulan UNIQUE (kode, bulan);

-- 4. Tambahkan Index untuk mempercepat pencarian dan filter
CREATE INDEX IF NOT EXISTS idx_karyawan_pabrik_kode ON public.karyawan_pabrik(kode);
CREATE INDEX IF NOT EXISTS idx_karyawan_pabrik_bulan ON public.karyawan_pabrik(bulan);

-- 5. Security & RLS
ALTER TABLE public.karyawan_pabrik ENABLE ROW LEVEL SECURITY;
DROP POLICY IF EXISTS "Public Access Karyawan" ON public.karyawan_pabrik;
CREATE POLICY "Public Access Karyawan" ON public.karyawan_pabrik FOR ALL USING (true) WITH CHECK (true);
GRANT ALL ON public.karyawan_pabrik TO anon, authenticated, service_role;

