-- Fix Table Karyawan Pabrik
CREATE TABLE IF NOT EXISTS public.karyawan_pabrik (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    kode TEXT NOT NULL,
    nama TEXT NOT NULL,
    jenis_kelamin TEXT DEFAULT 'L',
    grade_p1 TEXT,
    grade_p2 TEXT,
    divisi TEXT,
    bulan TEXT,
    keterangan TEXT,
    status_aktif BOOLEAN DEFAULT true,
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- Constraint
DO $$ BEGIN
    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'unique_karyawan_kode_bulan') THEN
        ALTER TABLE public.karyawan_pabrik ADD CONSTRAINT unique_karyawan_kode_bulan UNIQUE (kode, bulan);
    END IF;
END $$;

-- Security
ALTER TABLE public.karyawan_pabrik ENABLE ROW LEVEL SECURITY;
