-- 1. Buat Tabel Data Karyawan (Master)
CREATE TABLE IF NOT EXISTS public.data_karyawan_pabrik (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    kode TEXT NOT NULL UNIQUE,
    nama TEXT NOT NULL,
    jenis_kelamin TEXT DEFAULT 'L',
    grade_p1 TEXT,
    grade_p2 TEXT,
    divisi TEXT,
    bulan TEXT,
    keterangan TEXT,
    status_aktif BOOLEAN DEFAULT true,
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- 2. Buat Tabel Presensi Harian
CREATE TABLE IF NOT EXISTS public.presensi_harian_pabrik (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tanggal DATE NOT NULL,
    kode TEXT NOT NULL,
    grade_p1 TEXT,
    grade_p2 TEXT,
    bulan TEXT,
    kehadiran TEXT, -- 1, 0.5, S, I, A
    lembur TEXT,
    periode TEXT,
    perusahaan TEXT,
    keterangan TEXT,
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- 3. Setup RLS
ALTER TABLE public.data_karyawan_pabrik ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.presensi_harian_pabrik ENABLE ROW LEVEL SECURITY;

-- 4. Policies
CREATE POLICY "Public Access Karyawan" ON public.data_karyawan_pabrik FOR ALL USING (true) WITH CHECK (true);
CREATE POLICY "Public Access Presensi" ON public.presensi_harian_pabrik FOR ALL USING (true) WITH CHECK (true);

-- 5. Grants
GRANT ALL ON public.data_karyawan_pabrik TO anon, authenticated, service_role;
GRANT ALL ON public.presensi_harian_pabrik TO anon, authenticated, service_role;

NOTIFY pgrst, 'reload config';
