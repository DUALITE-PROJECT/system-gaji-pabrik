-- 1. Buat Tabel stock_opname_gudang
CREATE TABLE IF NOT EXISTS public.stock_opname_gudang (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    stock_opname_id BIGINT REFERENCES public.stock_opname(id) ON DELETE CASCADE,
    sku_id BIGINT REFERENCES public.master_sku(id),
    tanggal DATE,
    lokasi TEXT DEFAULT 'Gudang Utama',
    no_karung TEXT DEFAULT '-',
    qty_sistem NUMERIC DEFAULT 0,
    qty_fisik NUMERIC DEFAULT 0,
    selisih NUMERIC DEFAULT 0,
    status TEXT DEFAULT 'Pending',
    keterangan TEXT,
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- 2. Enable RLS
ALTER TABLE public.stock_opname_gudang ENABLE ROW LEVEL SECURITY;

-- 3. Buat Policy
DROP POLICY IF EXISTS "Public Access SO Gudang" ON public.stock_opname_gudang;
CREATE POLICY "Public Access SO Gudang" ON public.stock_opname_gudang FOR ALL USING (true) WITH CHECK (true);

-- 4. Grant Permissions
GRANT ALL ON public.stock_opname_gudang TO anon, authenticated, service_role;

-- 5. Refresh Cache
NOTIFY pgrst, 'reload config';
