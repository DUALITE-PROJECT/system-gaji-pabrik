-- 1. Table: input_data_produksi (Existing/Ensure Schema)
CREATE TABLE IF NOT EXISTS public.input_data_produksi (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tanggal DATE NOT NULL,
    kode TEXT,
    sku TEXT,
    qty NUMERIC DEFAULT 0,
    jumlah_orang NUMERIC DEFAULT 0,
    perusahaan TEXT,
    bagian TEXT,
    posisi TEXT,
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- Indexes for Production
CREATE INDEX IF NOT EXISTS idx_input_data_produksi_tanggal ON public.input_data_produksi(tanggal);
CREATE INDEX IF NOT EXISTS idx_input_data_produksi_kode ON public.input_data_produksi(kode);

-- 2. Table: input_maintenance (New)
CREATE TABLE IF NOT EXISTS public.input_maintenance (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tanggal DATE NOT NULL,
    kode TEXT NOT NULL,
    qty NUMERIC DEFAULT 0,
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW(),
    
    -- CONSTRAINT: Prevent duplicate Date + Code
    CONSTRAINT unique_tanggal_kode UNIQUE (tanggal, kode)
);

-- Indexes for Maintenance
CREATE INDEX IF NOT EXISTS idx_input_maintenance_tanggal ON public.input_maintenance(tanggal);
CREATE INDEX IF NOT EXISTS idx_input_maintenance_kode ON public.input_maintenance(kode);

-- 3. Security (RLS)
ALTER TABLE public.input_data_produksi ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.input_maintenance ENABLE ROW LEVEL SECURITY;

-- Policies (Allow Public Access for Demo/Internal App)
DROP POLICY IF EXISTS "Public Access Production Input" ON public.input_data_produksi;
CREATE POLICY "Public Access Production Input" ON public.input_data_produksi FOR ALL USING (true) WITH CHECK (true);

DROP POLICY IF EXISTS "Public Access Maintenance Input" ON public.input_maintenance;
CREATE POLICY "Public Access Maintenance Input" ON public.input_maintenance FOR ALL USING (true) WITH CHECK (true);

-- Grants
GRANT ALL ON public.input_data_produksi TO anon, authenticated, service_role;
GRANT ALL ON public.input_maintenance TO anon, authenticated, service_role;

