-- Pastikan tabel master_sku ada dan memiliki struktur yang benar
CREATE TABLE IF NOT EXISTS public.master_sku (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    kode_sku TEXT NOT NULL UNIQUE,
    nama TEXT NOT NULL,
    deskripsi TEXT,
    kategori TEXT DEFAULT 'Lainnya',
    satuan TEXT DEFAULT 'Pcs',
    min_stock NUMERIC DEFAULT 0,
    hpp NUMERIC DEFAULT 0,
    harga_jual NUMERIC DEFAULT 0,
    hpp_updated_at DATE,
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- Tambahkan kolom jika belum ada (Safe Alter)
ALTER TABLE public.master_sku ADD COLUMN IF NOT EXISTS hpp NUMERIC DEFAULT 0;
ALTER TABLE public.master_sku ADD COLUMN IF NOT EXISTS harga_jual NUMERIC DEFAULT 0;
ALTER TABLE public.master_sku ADD COLUMN IF NOT EXISTS hpp_updated_at DATE;
ALTER TABLE public.master_sku ADD COLUMN IF NOT EXISTS kategori TEXT DEFAULT 'Lainnya';
ALTER TABLE public.master_sku ADD COLUMN IF NOT EXISTS satuan TEXT DEFAULT 'Pcs';
ALTER TABLE public.master_sku ADD COLUMN IF NOT EXISTS min_stock NUMERIC DEFAULT 0;

-- Indexes & Security
CREATE INDEX IF NOT EXISTS idx_master_sku_kode ON public.master_sku(kode_sku);

ALTER TABLE public.master_sku ENABLE ROW LEVEL SECURITY;
