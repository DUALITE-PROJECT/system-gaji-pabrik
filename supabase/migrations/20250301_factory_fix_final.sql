-- 1. MASTER GAJI
CREATE TABLE IF NOT EXISTS public.master_gaji (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    bulan TEXT,
    grade TEXT,
    gaji_pokok NUMERIC DEFAULT 0,
    gaji_harian NUMERIC DEFAULT 0,
    gaji_setengah_hari NUMERIC DEFAULT 0,
    gaji_per_jam NUMERIC DEFAULT 0,
    lembur NUMERIC DEFAULT 0,
    uang_makan NUMERIC DEFAULT 0,
    uang_makan_harian NUMERIC DEFAULT 0,
    uang_kehadiran NUMERIC DEFAULT 0,
    uang_kehadiran_harian NUMERIC DEFAULT 0,
    bonus NUMERIC DEFAULT 0,
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW()
);
ALTER TABLE public.master_gaji ENABLE ROW LEVEL SECURITY;
DROP POLICY IF EXISTS "Public Access Gaji" ON public.master_gaji;
CREATE POLICY "Public Access Gaji" ON public.master_gaji FOR ALL USING (true) WITH CHECK (true);

-- 2. DATA KARYAWAN
CREATE TABLE IF NOT EXISTS public.data_karyawan_pabrik (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    kode TEXT NOT NULL,
    nama TEXT NOT NULL,
    jenis_kelamin TEXT DEFAULT 'L',
    grade_p1 TEXT,
    grade_p2 TEXT,
    divisi TEXT,
    bulan TEXT,
    keterangan TEXT,
    status_aktif BOOLEAN DEFAULT true,
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW()
);
ALTER TABLE public.data_karyawan_pabrik DROP CONSTRAINT IF EXISTS data_karyawan_pabrik_kode_key;
DO $$ BEGIN
    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'unique_kode_bulan') THEN
        ALTER TABLE public.data_karyawan_pabrik ADD CONSTRAINT unique_kode_bulan UNIQUE (kode, bulan);
    END IF;
END $$;
ALTER TABLE public.data_karyawan_pabrik ENABLE ROW LEVEL SECURITY;
DROP POLICY IF EXISTS "Public Access Karyawan" ON public.data_karyawan_pabrik;
CREATE POLICY "Public Access Karyawan" ON public.data_karyawan_pabrik FOR ALL USING (true) WITH CHECK (true);

